#include <stdio.h>

typedef unsigned int UINT;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : OffBit
//  Description   : Write a program which accept one number from user & off 7th
//                  bit of that number if it is on.Return modified number.
//  Input         : Integer
//  Output        : Integer
//  Author        : Neha Navin Desai
//  Date          : 05/02/2026
//
////////////////////////////////////////////////////////////////////////////////

UINT OffBit(UINT NO)
{
    UINT Mask = 0;

    Mask = 1 << 6;

    Mask = ~Mask;

    return NO & Mask;
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    UINT iNo = 0;
    UINT iRet = 0;

    printf("Enter the number : \n");
    scanf("%u", &iNo);

    iRet = OffBit(iNo);

    printf("Modified number is : %d\n", iRet);

    return 0;
}

