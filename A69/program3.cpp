#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Write a recursive program which display below patten.
//  Input         : Integer
//  Output        : 1   2   3   4   5
//  Author        : Neha Navin Desai
//  Date          : 06/02/2026
//
////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    
    if (iNo != 0)
    {
        cout <<iNo<<"\t";
        iNo--;
        Display(iNo);
    }
}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    cout << "Enter the number : " << "\n";
    cin >> iValue;

    Display(iValue);

    return 0;
}

