#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Displays numbers from n to 1 recursively
//  Input         : Integer
//  Output        : void
//  Time Complexity : O(N)
//
////////////////////////////////////////////////////////////
void Display(int iNo)
{
    if (iNo >= 1)
    {
        cout << iNo << " * ";
        iNo--;
        Display(iNo);

    }
}

////////////////////////////////////////////////////////////
//
//  ENTRY POINT FUNCTION FOR THE APPLICATION
//
////////////////////////////////////////////////////////////
int main()
{
    int iValue;

    cout << "Enter number: ";
    cin >> iValue;

    Display(iValue);
    cout << "\n";

    return 0;
}

