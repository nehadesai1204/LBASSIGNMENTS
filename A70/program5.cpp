int Mult(int iNo)
{
static int iMult = 1;
static int iDigit = 0;

if (iNo != 1)
{
    iDigit = iNo % 10;
    iMult = iMult * iDigit;
    Mult(iNo / 10);
}
return iMult;

}

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    cout << "Enter the number : " << "\n";
    cin >> iValue;

    iRet = Mult(iValue);

    cout << iRet;

    return 0;
}

